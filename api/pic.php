function get_sitemeta($url) {
     $data = file_get_contents($url);
     if (!empty($data)) {
          preg_match('/"imgurl":"([\w\W]*?)"/si', $data, $matches);
     }
     if (empty($matches[1])) {
          preg_match('/"imgurl":\s+"([\w\W]*?)"/si', $data, $matches);
     }
     if (empty($matches[1])) {
          preg_match('/"img":"([\w\W]*?)"/si', $data, $matches);
     }
     return $matches[1];
}